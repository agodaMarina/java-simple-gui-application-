package Models;

public class Classe {
    private String code;
    private String intitutle;

    public Classe(String code) {
        this.code = code;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getIntitutle() {
        return intitutle;
    }

    public void setIntitutle(String intitutle) {
        this.intitutle = intitutle;
    }
}
